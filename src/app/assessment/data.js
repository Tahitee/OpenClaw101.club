export function getLevel(total) {
  if (total >= 210) return 'green'
  if (total >= 140) return 'yellow'
  return 'red'
}

export function getDimColor(pct) {
  if (pct >= 75) return '#10b981'
  if (pct >= 50) return '#f59e0b'
  return '#ef4444'
}

export function getDimComment(pct, name) {
  if (pct >= 75) return `${name}表现良好，具备相应条件。`
  if (pct >= 50) return `${name}需要改进，建议加强相关能力建设。`
  return `${name}是明显短板，需优先解决后再考虑引入。`
}
